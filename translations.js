// Function to translate general keys, including nested keys
export const translate = (key, language) => {
  // Split the key into parts to access nested keys if needed
  const keys = key.split('.');
  
  // Start with the translations for the selected language
  let translation = translations[language];

  // Traverse the keys to get to the correct translation
  for (const k of keys) {
    if (translation && k in translation) {
      translation = translation[k];
    } else {
      return key; // Return the original key if not found
    }
  }

  return translation || key; // Return the found translation or the original key
};

// Function to translate category keys
export const translateCategory = (category, language) => {
  return translations[language].categories[category] || category;
};

// Function to translate description keys
export const translateDescription = (description, language) => {
  return translations[language].descriptions[description] || description;
};
